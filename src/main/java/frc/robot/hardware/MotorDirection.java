package frc.robot.hardware;

/**
 * The direction a Motor should spin in.
 * @see frc.robot.hardware.IMotor
 */
public enum MotorDirection {
    /**
     * Do not spin the motor.
     */
    STOPPED,
    /**
     * Spin the motor forwards.
     */
    FORWARD,
    /**
     * Spin the Motor in reverse.
     */
    REVERSE    
}
